package programmers.days0206;

/*
 
 문제 설명
정수 num과 n이 매개 변수로 주어질 때, num이 n의 배수이면 1을 return 
n의 배수가 아니라면 0을 return하도록 solution 함수를 완성해주세요.
 
 */
public class Lv0_16 {

	//  int num = 98; // 입력 예시
	//  int n = 2;    // 입력 예시
	public int solution(int num, int n) {
		 int answer = 0;
		 if((num % n) == 0) {
			 
			return answer = 1;
		  
			 
		 }
		 else {
			return answer = 0;
		 }  
	       
	    
	 }
}
